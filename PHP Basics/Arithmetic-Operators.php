<?php
// + is an Arithmetic Operator
$First_Variable = 70;
$Second_Variable = 20;
$sum = $First_Variable + $Second_Variable;
echo "Sum = ".$sum;
echo "<br>";
// "/" is an Arithmetic Operator
$third_Variable = 70;
$Fourth_Variable = 20;
$div = $third_Variable / $Fourth_Variable;
echo "Division = ".$div;
echo "<br>";
// * is an Arithmetic Operator
$fifth_Variable = 70;
$Six_Variable = 20;
$mul = $fifth_Variable * $Six_Variable;
echo "Multiply = ".$mul;
echo "<br>";
// - is an Arithmetic Operator
$seven_Variable = 70;
$eight_Variable = 20;
$min = $seven_Variable - $eight_Variable;
echo "Subtraction = ".$min;

?>